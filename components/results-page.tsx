"use client"

import { useCallback } from "react"
import type { DiagnosticResult } from "@/lib/decision-tree"
import type { LogEntry } from "./troubleshooter"
import {
  Card,
  CardContent,
  CardDescription,
  CardFooter,
  CardHeader,
  CardTitle,
} from "@/components/ui/card"
import { Button } from "@/components/ui/button"
import { Badge } from "@/components/ui/badge"
import { Progress } from "@/components/ui/progress"
import {
  CheckCircle2,
  AlertTriangle,
  AlertCircle,
  Download,
  RotateCcw,
  Wrench,
  Shield,
} from "lucide-react"

const severityConfig = {
  low: {
    icon: <CheckCircle2 className="size-5" />,
    color: "text-success",
    bg: "bg-success/10",
    border: "border-success/20",
    label: "Low Severity",
    progressColor: "bg-success",
  },
  medium: {
    icon: <AlertTriangle className="size-5" />,
    color: "text-warning",
    bg: "bg-warning/10",
    border: "border-warning/20",
    label: "Medium Severity",
    progressColor: "bg-warning",
  },
  high: {
    icon: <AlertCircle className="size-5" />,
    color: "text-destructive",
    bg: "bg-destructive/10",
    border: "border-destructive/20",
    label: "High Severity",
    progressColor: "bg-destructive",
  },
}

interface ResultsPageProps {
  result: DiagnosticResult
  logEntries: LogEntry[]
  onReset: () => void
}

export function ResultsPage({ result, logEntries, onReset }: ResultsPageProps) {
  const severity = severityConfig[result.severity]

  const handleExportPDF = useCallback(() => {
    const lines: string[] = []
    lines.push("=================================================")
    lines.push("  SMART HOME NETWORK TROUBLESHOOTER - REPORT")
    lines.push("=================================================")
    lines.push("")
    lines.push(`Date: ${new Date().toLocaleDateString("en-US", { weekday: "long", year: "numeric", month: "long", day: "numeric" })}`)
    lines.push(`Time: ${new Date().toLocaleTimeString()}`)
    lines.push("")
    lines.push("-------------------------------------------------")
    lines.push("  DIAGNOSIS RESULT")
    lines.push("-------------------------------------------------")
    lines.push("")
    lines.push(`Issue: ${result.title}`)
    lines.push(`Confidence: ${result.matchPercentage}%`)
    lines.push(`Severity: ${result.severity.toUpperCase()}`)
    lines.push("")
    lines.push(`Description:`)
    lines.push(result.description)
    lines.push("")
    lines.push("-------------------------------------------------")
    lines.push("  RECOMMENDED FIX ACTIONS")
    lines.push("-------------------------------------------------")
    lines.push("")
    result.fixActions.forEach((action, i) => {
      lines.push(`  ${i + 1}. ${action}`)
    })
    lines.push("")
    lines.push("-------------------------------------------------")
    lines.push("  TROUBLESHOOTING LOG")
    lines.push("-------------------------------------------------")
    lines.push("")
    logEntries.forEach((entry) => {
      const time = entry.timestamp.toLocaleTimeString("en-US", {
        hour: "2-digit",
        minute: "2-digit",
        second: "2-digit",
        hour12: false,
      })
      const typeLabel =
        entry.type === "answer"
          ? "RESPONSE"
          : entry.type === "thought"
          ? "ANALYSIS"
          : "RESOLVED"
      lines.push(`[${time}] [${typeLabel}] ${entry.category}`)
      lines.push(`  ${entry.content}`)
      lines.push("")
    })
    lines.push("=================================================")
    lines.push("  End of Report - Generated by Smart Home Network Troubleshooter")
    lines.push("=================================================")

    const blob = new Blob([lines.join("\n")], { type: "text/plain" })
    const url = URL.createObjectURL(blob)
    const a = document.createElement("a")
    a.href = url
    a.download = `network-diagnostic-${new Date().toISOString().split("T")[0]}.txt`
    document.body.appendChild(a)
    a.click()
    document.body.removeChild(a)
    URL.revokeObjectURL(url)
  }, [result, logEntries])

  return (
    <div className="flex flex-col gap-5">
      {/* Success header */}
      <div className="flex items-center gap-3">
        <div className="flex size-10 items-center justify-center rounded-full bg-success/10">
          <Shield className="size-5 text-success" />
        </div>
        <div>
          <h2 className="text-lg font-semibold text-foreground">
            Diagnosis Complete
          </h2>
          <p className="text-sm text-muted-foreground">
            We identified the most likely cause of your issue.
          </p>
        </div>
      </div>

      {/* Main result card */}
      <Card className={`border-2 ${severity.border} shadow-lg`}>
        <CardHeader>
          <div className="flex flex-wrap items-center gap-3">
            <Badge
              variant="outline"
              className={`gap-1.5 ${severity.color} ${severity.border}`}
            >
              {severity.icon}
              {severity.label}
            </Badge>
            <Badge variant="secondary" className="gap-1 font-mono text-sm">
              {result.matchPercentage}% Match
            </Badge>
          </div>
          <CardTitle className="text-2xl leading-tight mt-2">
            {result.title}
          </CardTitle>
          <CardDescription className="text-base leading-relaxed">
            {result.description}
          </CardDescription>
        </CardHeader>
        <CardContent>
          {/* Match confidence bar */}
          <div className="mb-6">
            <div className="mb-2 flex items-center justify-between">
              <span className="text-xs font-medium text-muted-foreground">
                Diagnostic Confidence
              </span>
              <span className="text-sm font-semibold text-foreground">
                {result.matchPercentage}%
              </span>
            </div>
            <div className="relative h-3 w-full overflow-hidden rounded-full bg-secondary">
              <div
                className={`h-full rounded-full transition-all duration-1000 ease-out ${severity.progressColor}`}
                style={{ width: `${result.matchPercentage}%` }}
              />
            </div>
          </div>

          {/* Fix Actions */}
          <div className="rounded-lg border border-border bg-secondary/30 p-4">
            <div className="mb-3 flex items-center gap-2">
              <Wrench className="size-4 text-primary" />
              <h3 className="text-sm font-semibold text-foreground">
                Recommended Fix Actions
              </h3>
            </div>
            <div className="flex flex-col gap-3">
              {result.fixActions.map((action, index) => (
                <div
                  key={index}
                  className="flex items-start gap-3 rounded-md bg-card p-3 border border-border"
                >
                  <div className="flex size-6 shrink-0 items-center justify-center rounded-full bg-primary text-xs font-bold text-primary-foreground">
                    {index + 1}
                  </div>
                  <p className="text-sm text-foreground leading-relaxed">
                    {action}
                  </p>
                </div>
              ))}
            </div>
          </div>
        </CardContent>
        <CardFooter className="flex flex-col gap-3 sm:flex-row">
          <Button
            onClick={handleExportPDF}
            variant="outline"
            className="w-full gap-2 sm:w-auto"
          >
            <Download className="size-4" />
            Export Log for Support
          </Button>
          <Button onClick={onReset} className="w-full gap-2 sm:w-auto">
            <RotateCcw className="size-4" />
            Run New Diagnosis
          </Button>
        </CardFooter>
      </Card>
    </div>
  )
}
